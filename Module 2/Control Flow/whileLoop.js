let odd = 1
while (odd <= 10) {
    console.log(`Odd value  is ${odd}`);
    odd = odd + 2
}

let even = 0
while (even < 10) {
    console.log(`Even Value is ${even}`);
    even = even + 2
}

let carModel = ['Honda', 'Kia', 'MG'];

let index = 0;

while (index < carModel.length) {
    console.log(`Car Model is ${carModel[index]}`);
    index++
}

